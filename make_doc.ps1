$word = New-Object -ComObject Word.Application
$word.Visible = $false
$doc  = $word.Documents.Add()
$sel  = $word.Selection

# Цвета (Word использует BGR как long)
$CLR_GRAY   = 15921906  # #F2F2F2 — фон кода
$CLR_HEAD   = 14540253  # #DDE8DD — заголовки таблиц
$CLR_WHITE  = 16777215

# ── Вспомогательные функции ───────────────────────────────────────

function H1($t) {
    $sel.Style = $doc.Styles.Item(-2)   # Heading 1
    $sel.TypeText($t); $sel.TypeParagraph()
}
function H2($t) {
    $sel.Style = $doc.Styles.Item(-3)   # Heading 2
    $sel.TypeText($t); $sel.TypeParagraph()
}
function H3($t) {
    $sel.Style = $doc.Styles.Item(-4)   # Heading 3
    $sel.TypeText($t); $sel.TypeParagraph()
}
function P($t) {
    $sel.Style = $doc.Styles.Item(1)    # Normal
    $sel.TypeText($t); $sel.TypeParagraph()
}
function BR() {
    $sel.Style = $doc.Styles.Item(1); $sel.TypeParagraph()
}
function Bullet($t) {
    $sel.Style = $doc.Styles.Item(-11)  # List Bullet
    $sel.TypeText($t); $sel.TypeParagraph()
    $sel.Style = $doc.Styles.Item(1)
}
function Bullet2($t) {
    $sel.Style = $doc.Styles.Item(-12)  # List Bullet 2
    $sel.TypeText($t); $sel.TypeParagraph()
    $sel.Style = $doc.Styles.Item(1)
}

function Code($lines) {
    $startPos = $sel.Range.Start
    foreach ($line in $lines) {
        $sel.Style = $doc.Styles.Item(1)
        if ($line -eq "") { $sel.TypeText(" ") } else { $sel.TypeText($line) }
        $sel.TypeParagraph()
    }
    $endPos = $sel.Range.Start - 1
    $r = $doc.Range($startPos, $endPos)
    $r.Font.Name = "Consolas"
    $r.Font.Size = 9
    $r.ParagraphFormat.LeftIndent  = $word.InchesToPoints(0.25)
    $r.ParagraphFormat.RightIndent = $word.InchesToPoints(0.25)
    $r.ParagraphFormat.SpaceBefore = 3
    $r.ParagraphFormat.SpaceAfter  = 3
    $r.Shading.BackgroundPatternColor = $CLR_GRAY
}

function Table($headers, $data) {
    $numRows = $data.Count + 1
    $numCols = $headers.Count
    $tbl = $doc.Tables.Add($sel.Range, $numRows, $numCols)
    $tbl.Borders.Enable = $true

    # Заголовок
    for ($c = 1; $c -le $numCols; $c++) {
        $cell = $tbl.Cell(1, $c)
        $cell.Range.Text = $headers[$c-1]
        $cell.Range.Font.Bold  = $true
        $cell.Range.Font.Size  = 10
        $cell.Range.Font.Color = 0
        $cell.Shading.BackgroundPatternColor = $CLR_HEAD
    }
    # Данные
    for ($r = 0; $r -lt $data.Count; $r++) {
        $row = $data[$r]
        for ($c = 1; $c -le $numCols; $c++) {
            $cell = $tbl.Cell($r+2, $c)
            $cell.Range.Text      = $row[$c-1]
            $cell.Range.Font.Size = 10
            $cell.Range.Font.Bold = $false
            if ($r % 2 -eq 1) {
                $cell.Shading.BackgroundPatternColor = 16119285  # #F5F5F5
            }
        }
    }
    $tbl.Columns.AutoFit()

    # Перемещаем курсор за таблицу
    $endPos = $tbl.Range.End
    $sel.SetRange($endPos, $endPos)
    $sel.TypeParagraph()
}

